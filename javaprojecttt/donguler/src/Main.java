public class Main {

    public static void main(String[] args) {

        //For
        for (int i=1; i<10; i++){
            System.out.println(i);
        }
        System.out.println("döngü bitti");
        for (int i=1; i<10; i+=2){
            System.out.println(i);
        }
        //while
        int i = 1;
        while(i<10){
            System.out.println(i);
            i=i+1;
        }
        System.out.println("döngü bitti");

        // do-while
        int a = 1;
        do{
                System.out.println(a);
                a=a+1;
            }while(a<10);
        }
    }

